    @Before
    fun setup(){
        apiService = Retrofit
            .Builder()
            .baseUrl(BuildConfig.SERVER_URL)
            .addConverterFactory(GsonConverterFactory.create())
            .build()
            .create(ApiService::class.java)
    }

    @Test
    fun `get all coins`()= runTest {
        val pageSize =50
        val result = apiService.getCoinList(
            vsCurrency = "USD",
            order = "market_cap_desc",
            page = 1,
            pageSize = pageSize,
            ids = "")
        val size = result.size
        val firstId = result.first().id

        assertThat(size).isEqualTo(pageSize)
        assertThat(firstId).isEqualTo("bitcoin")
    }


    @Test
    fun `test call by ids`() = runTest{
        var ids = bigIdsList.take(49)
        println(ids.size)
        val result = apiService.getCoinList(
            vsCurrency = "USD",
            order = "market_cap_desc",
            page = 1,
            pageSize = 50,
            ids = ids.removeBracket()
        )

        repeat(ids.size){
            println("${ids[it]}, ${result[it].id}, ${result[it].marketCapRank}")
        }
        assertThat(result.size).isAtLeast(1)
    }

    @Test
    fun `search by params`() = runTest {
        val result = apiService.search("")

        result.coins?.forEach {
            println("${ it?.id } ${it?.marketCapRank}")
        }
        println("SIZE ${result.coins?.size}")

        assertThat(true)
    }



